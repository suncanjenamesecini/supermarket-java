    int i = 0;
    Double Uprice, prodTotal=0.0,GrdTotal=0.0;
    int AvailQty;
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        
        if(BillQty.getText().isEmpty() || BillName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information");
        } else if (AvailQty <= Integer.valueOf(BillQty.getText())) {
            JOptionPane.showMessageDialog(this,"Not Enough in Stock");
        }
        else {
            i++;
            prodTotal = Uprice*Double.valueOf(BillQty.getText());
            GrdTotal += prodTotal;
            if(i == 1) {
               BillText.setText(BillText.getText()+"\t=============FAMILIY POINT================\n"+"\t   NUM      PRODUCT      PRICE     QUANTITY  TOTAL\n\t  "+i+"             "+BillName.getText()+"            "+Uprice+"            "+BillQty.getText()+"           "+prodTotal+"\n"); 
            } else {
                BillText.setText(BillText.getText()+"\t  "+i+"             "+BillName.getText()+"            "+Uprice+"            "+BillQty.getText()+"           "+prodTotal+"\n");
            }
            GrandT.setText("Rs   "+GrdTotal);
            update();
        }
    }//GEN-LAST:event_AddBtnMouseClicked

