    int prid, newQty = 0;
    public void update() {
        try {
                newQty = AvailQty - Integer.valueOf(BillQty.getText());
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/SuperMarketdb","User1","ana");
                String Query = "Update User1.PRODUCTTBL set PRODQTY="+newQty+"where PRODID="+prid;
                AvailQty = newQty;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query);
                //JOptionPane.showMessageDialog(this, "Product Updated");
                SelectBill();
            } catch (SQLException e) {
                e.printStackTrace();
            }
    }
    
    private void GetCat() {
        try {
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/SuperMarketdb","User1","ana");
            St = Con.createStatement();
            String Query = "Select * from User1.CATEGORYTBL";
            Rs = St.executeQuery(Query);
            while(Rs.next()) {
                String MyCat = Rs.getString("CATNAME");
                CatCb.addItem(MyCat);
            }
        } catch (Exception e ) {
            e.printStackTrace();
        }
    }

